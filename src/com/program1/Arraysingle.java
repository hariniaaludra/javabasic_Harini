package com.program1;

public class Arraysingle {
	public static void main(String[]args) {
		
		int a[]= {10,20,30,40,50};
		
		
		for(int i=0;i<a.length;i++) // we can declare this in for each loop[eg:for(int x:a)]
		{
			
				
			
			System.out.println(a[i]); //[eg:System.out.println(x);]
		
		
		}
		}
}                                     



		
	
