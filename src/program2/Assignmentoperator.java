package program2;

public class Assignmentoperator {
	public static void main(String[]args) {
		int a=6;
		int b=10;
		int c=20;
		int d=30;
		a+=4;// a+=a;
		b*=5;//b*=a;
		c/=6;//c/=a;
		d%=10;//d+=a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
