package program2;

public class Logicaloperator {
	public static void main(String[]args) {
		int a=10;
		int b=20; 
		int c=30;
		if((a>5 && a<20)) {
		System.out.println(a>5 && a<20);
		}
		else if((b<10 || b<10)) {
		System.out.println(b<10 || b<10);
	}
		else if(!(c>20 && c<40)){
		System.out.println(!(c>20 && c<40));
		}
		else if(!(c>10)|| (c<20)){
		System.out.println(!(c>10) || (c<20));
		}
	

}}
